function openModal(note, wc) {
    // Require extra password for note 21
    if (note.number === 21 && sessionStorage.getItem(NOTE21_PW_KEY) !== '1') {
        showNote21Screen(() => openModal(note, wc));
        return;
    }
    const untitled = isUntitledTitle(note.title);
    modalTitle.textContent = untitled ? '[sin título]' : (note.title || `nota ${note.number}/${note.of}`);
    modalTitle.classList.toggle('untitled', untitled);
    // safe content insertion: escape then convert newlines to <br>
    const safeBody = escapeHtml(note.body).replace(/\n/g,'<br>');
    const metaHtml = ` <div class="note-meta-large">nota ${note.number}/${note.of} · ${note.date} ${note.time} · ${wc} palabras ${note.draft ? '<span class="note-draft">borrador</span>' : ''}</div>`;
    modalBody.innerHTML = metaHtml + `<div class="note-full">${safeBody}</div>`;
    modal.classList.remove('hidden');
    // reset scroll position for each note open
    modalBody.scrollTop = 0;
    // move focus to modal body so keyboard users can scroll with arrow keys
    modalBody.focus();

    // Unlock note 21 when the reader reaches the end of note 20
    if (note.number === 20 && sessionStorage.getItem(NOTE21_UNLOCK_KEY) !== '1') {
        const maybeUnlock = () => {
            const atBottom = Math.ceil(modalBody.scrollTop + modalBody.clientHeight) >= (modalBody.scrollHeight - 8);
            if (atBottom) {
                sessionStorage.setItem(NOTE21_UNLOCK_KEY, '1');
                modalBody.removeEventListener('scroll', onScroll);
                renderNotes();
                showToast('Nota 21 desbloqueada ✨');
            }
        };
        const onScroll = () => maybeUnlock();
        // if content isn't scrollable, unlock immediately after render
        if (modalBody.scrollHeight <= modalBody.clientHeight + 4) {
            sessionStorage.setItem(NOTE21_UNLOCK_KEY, '1');
            renderNotes();
            showToast('Nota 21 desbloqueada ✨');
        } else {
            // attach listener and also run a first check
            modalBody.addEventListener('scroll', onScroll);
            maybeUnlock();
        }
    }
}

