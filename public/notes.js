function countWords(text) {
    if (!text) return 0;
    return text.trim().split(/\s+/).filter(Boolean).length;
}

function isUntitledTitle(title) {
    // treat empty/whitespace, or variants of "[sin título]" / "sin titulo" as untitled
    if (!title || !String(title).trim()) return true;
    const t = String(title).trim().toLowerCase()
        .normalize('NFD').replace(/[\u0300-\u036f]/g, ''); // strip accents
    // remove optional leading/trailing brackets
    const stripped = t.replace(/^\[\s*/, '').replace(/\s*\]$/, '').trim();
    return stripped === 'sin titulo';
}

function makePreview(text, max=140) {
    if (!text) return '';
    return text.replace(/\s+/g, ' ').trim().slice(0, max) + (text.length>max ? '…' : '');
}

